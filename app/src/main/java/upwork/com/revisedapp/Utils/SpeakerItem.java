public class SpeakerItem {
    private String speakerRoll, speakerName, speakerPosition, speakerJob, title;
    private int photoURL;

    public int getPhotoURL() {
        return photoURL;
    }

    public void setPhotoURL(int photoURL) {
        this.photoURL = photoURL;
    }

    public String getSpeakerRoll() {
        return speakerRoll;
    }

    public void setSpeakerRoll(String speakerRoll) {
        this.speakerRoll = speakerRoll;
    }

    public String getSpeakerName() {
        return speakerName;
    }

    public void setSpeakerName(String speakerName) {
        this.speakerName = speakerName;
    }

    public String getSpeakerPosition() {
        return speakerPosition;
    }

    public void setSpeakerPosition(String speakerPosition) {
        this.speakerPosition = speakerPosition;
    }

    public String getSpeakerJob() {
        return speakerJob;
    }

    public void setSpeakerJob(String speakerJob) {
        this.speakerJob = speakerJob;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
